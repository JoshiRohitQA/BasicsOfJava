package basicprograms;

public class PrimitiveAssing {
	public static void main(String[] args) {
		
		byte age = 22;
		
		short age1 = age; //implicit type casting widening
		System.out.println(age1);
		
		short age2 = (short)age; //explicit type casting widening
		System.out.println(age2);
		
	
		
		
	}

}
